# 3. Գրել ծրագիր, որը․
#    - կֆիլտրի տրված list-ը filter և lambda ֆունկցիաների միջոցով և կթողնի միայն 3-ի բաժանվող զույգ թվերը,
#    օրինակ՝ nums = [1, 2, 6, 17, 18, 22, 23, 26, 30, 45],
#    - կտպի ստացված որպես լիստ,
#    տվյալ օրինակում պետք է տպի՝ [6, 18, 30]։

nums = [1, 2, 6, 17, 18, 22, 23, 26, 30, 45]

f = filter(lambda x: x % 3 == 0 and x % 2 == 0, nums)
print(list(f))