# 1. Գրել ծրագիր, որը․
#    - կտպի հետևյալ tuple-ների էլեմենտ առ էլեմենտ գումարը,
#    օրինակ՝ tuple_1 = (1, 2, -3, 4),
#            tuple_2 = (3, 5, 2, 1),
#            tuple_3 = (2, -2, 0, 1),
#            արդյունքը պետք է լինի՝
#            (6, 5, -1, 6)։

tuple_1 = (1, 2, -3, 4)
tuple_2 = (3, 5, 2, 1)
tuple_3 = (2, -2, 0, 1)
for el1, el2, el3 in zip(tuple_1,tuple_2,tuple_3):
    print(el1 + el2 + el3, end=' ')
