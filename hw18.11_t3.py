# 3․ Գրել ֆունկցիա, որը․
#    - կսահմանի աստիճան բարձրացնելու ֆունկցիա power(x, y), որը կվերադարձնի x ի y աստիճանը,
#    - Եթե y չտրվի այդ ֆունկցիային` power(x), ապա ֆունկցիան պետք է վերադարձնի x ի 2 աստիճանը,
#    - հաշվի առեք բացառությունները։

def power(x, y=2):
    if (x == 0):
        return "Wrong X"
    return x ** y

print(power(27, 1/3))