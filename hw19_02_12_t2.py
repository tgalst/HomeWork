# 2. Գրել ծրագիր, որը․
#    - կբազմապատկի տրված list-ի բոլոր էլեմենտները տրված թվով map և lambda ֆունկցիաների միջոցով,
#    օրինակ՝ nums = [1, 2, -4, 8, -9], n = 3,
#    - կտպի ստացվածը որպես լիստ,
#    տվյալ օրինակում պետք է տպի՝ [3, 6, -12, 24, -27]։


nums = [1, 2, -4, 8, -9]
n = 3
m = map(lambda x: x * n, nums)
print(list(m))